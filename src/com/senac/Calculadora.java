package com.senac;

public class Calculadora {

    public static double calcularAreaDoQuadrado(double ladoQuadrado) {
        return ladoQuadrado * ladoQuadrado;
    }

    public static double cacularAreaDoTriangulo(double ladoQuadrado) {
        return ladoQuadrado * ladoQuadrado / 2;
    }

    public static double calcularAreaDoCirculo(double ladoQuadrado) {
        return ((ladoQuadrado * ladoQuadrado) / 2) * 3.14;
    }
}
